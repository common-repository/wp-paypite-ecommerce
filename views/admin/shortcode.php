<div class="paypite-shortcode">
	<h1 class="paypite-text-purple">Les options du shortcode</h1>
	
	<p>
		Pour afficher une fiche produit sur votre site, insérez simplement ce shortcode à l'endroit où vous voulez que celle-ci devra s'afficher.
	</p>
	
	<p>
		<strong>[paypite id="ID_PRODUIT" show_details="YES/NO"]</strong>
	</p>
	
	<p>
		<strong>id="ID_PRODUIT"</strong><br />
		Correspond à l'ID du produit, cette information est généré automatiquement par le plugin
	</p>
	
	<p>
		<strong>show_details="YES/NO"</strong><br />
		Si <strong>YES</strong>: affichera les informations sur le produit au dessus du bouton de paiement (nom du produit, prix et description)<br />
		Si <strong>NO</strong>: affichera seulement sur le bouton de paiement
	</p>
</div>